$('.open_menu').on('click', function(){
	$('.list-item').toggleClass('menu_0');
	$('.list-item').toggleClass('zIndex101');
	$('.hidden_menu').toggleClass('zIndex100');
	$('.hidden_menu').toggleClass('opacity_1');
})
$('.hidden_menu').on('click', function(){
	$('.list-item').toggleClass('zIndex101');
	$('.list-item').toggleClass('menu_0');
	$('.hidden_menu').toggleClass('zIndex100');
	$('.hidden_menu').toggleClass('opacity_1');
})






















































